  function sort(data=[],order=[]){
    var tempArray=[];
    data.map((data)=>{
      for(var i=0;i<order.length;i++){
        if(data.Status === order[i]){
          for(var j=tempArray.length;j<=i;j++)
                tempArray.push([]);
          tempArray[i].push(data);
        }
      }
    })
    return tempArray.flat();
  }

